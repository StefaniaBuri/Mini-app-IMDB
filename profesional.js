class Professional{

    constructor( id, name, age, genre, weight, height, hairColor, eyeColor, race,
         isRetired, nationality, oscarNumbers, profession){
            this.id=id;
            this.name=name;
            this.age=age;
            this.genre=genre;
            this.weight=weight;
            this.height=height;
            this.hairColor=hairColor;
            this.eyeColor=eyeColor;
            this.race=race;
            this.isRetired=isRetired;
            this.nationality=nationality;
            this.oscarNumbers=oscarNumbers;
            this.profession=profession;
        }
}






module.exports = {Professional}